#include "libft.h"

char	*ft_strnstr(const char *haystack, const char *needle, size_t len)
{
	size_t	len2;

	if (*needle == '\0')
		return ((char *)haystack);
	len2 = ft_strlen(needle);
	while (*haystack != '\0' && len >= len2)
	{
		if (!ft_strncmp(haystack, needle, len2))
			return ((char *)haystack);
		haystack++;
		len--;
	}
	return (0);
}
